public class Remedio {
    private String nome;
    private int valor;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

    public int getValor(){
        return this.valor;
    }
}
